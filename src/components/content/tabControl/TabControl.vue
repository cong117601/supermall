<template>
  <!--只是文字不一样 所以不需要使用插槽，直接使用文字数组 遍历-->
  <div class="tab-control">
    <div
      v-for="(item,index) in titles"
      class="tab-control-item"
      :class="{active: index === currentIndex}"
      @click="itemClick(index)"
    >
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
export default {
  name: "TabControl",
  props: {
    titles: {
      type: Array,
      defalut() {
        return [];
      }
    }
  },
  data() {
    return {
      currentIndex: 0
    };
  },
  methods: {
    itemClick(index) {
      this.currentIndex = index;
      //把点击 传出去
      //console.log(index);
      
      this.$emit('tabClick',index)
    }
  }
};
</script>

